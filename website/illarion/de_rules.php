<h2>Spielregeln</h2>

<p>1. Jedem Spieler steht nur ein Account zu. Der Besitzer eines 
Accounts ist verantwortlich für alle Aktionen seiner Charaktere, 
auch wenn sich ein Dritter Zugriff auf den Account verschafft hat.</p>

<p>2. Wenn zwei Spieler über dieselbe Internetverbindung ins 
Spiel einloggen wollen, ist dies beim Team von Illarion anzumelden. </p>

<p>3. Die Namen von Charakteren müssen in eine mittelalterliche 
Fantasywelt und zu der Rasse des Charakters passen. Bekannte 
Namen aus Literatur, Geschichte oder Mythologie, sowie anstößige 
Namen sind nicht erlaubt, genausowenig wie Titel (Titel können im 
Spiel erworben werden).</p>

<p>4. Bei der Auswahl der Rassen eines Charakters ist die Auswahl 
auf die bei der Charaktererstellung angegebenen Rassen begrenzt. 
Auch über das Rollenspiel ist es nicht erlaubt, Mischlingscharaktere 
irgendeiner Art zu spielen.. Bei magischem und göttlichem Wirken 
oder übernatürlichen Eigenschaften gilt, dass alles, was man 
nicht über die Engine von Illarion umsetzen kann, eine Genehmigung 
eines Gamemasters erfordert.</p>

<p>5. Unabhängig von denen durch die Engine vorgegebenen Begrenzungen 
und Möglichkeiten muss sich ein Charakters an den “Realismus” der 
Spielwelt anpassen. Das beinhaltet ein Verhalten, dass jemand in 
einer mittelalterlichen Fantasiewelt an den Tag legen würde. 
Ignoriere keine NPCs und lass deine Verletzungen nicht ausser 
acht. Wenn ein Charakter niedergeschlagen oder verwundet wird, 
dann sind Verletzungen solange auszuspielen, bis die Lebenspunkte 
des Charakters wieder hergestellt sind.</p>

<p>6. Es ist nicht gestattet, dass zwei Charaktere eines Spielers 
miteinander interagieren. Die charakterer eines Spielers dürfen 
sich nicht gegenseitig kennen und ürfen keine Gegenstände austauschen, 
weder direkt noch über einen Mittelsmann. Es ist nicht gestattet sich 
mit zwei Charakteren gleichzeitig einzuloggen.</p>

<p>7. Die Verwendung von Out of Character (OOC) Wissen das der Spieler, 
jedoch nicht der Charakter, erlangt hat ist nicht erlaubt. Aktionen im 
Spiel dürfen nicht durch OOC-Einflüsse beeinflusst sein.</p>

<p>8. Die Sprache eines Charakters sollte einer mittelalterlichen Fantasiewelt  
angemessen sein . "Leet-Speak" (z.B. "u g0t pwn3d") und moderne Ausdrücke 
sind zu unterlassen. Sinnlose Nachrichten und die durchgehende Verwendung 
von Großbuchstaben sind nicht erlaubt.</p>

<p>9. Aktionen eines Charakters (z.B. Kämpfen, Handwerk) sind durch Emotes 
einzuleiten und zu begleiten. Emotes beschreiben nur wahrnehmbare Handlungen 
und Zustände. Emotes enthalten keine Meinungen, Gedanken und Gefühle des Charakters.</p>

<p>10. So genannte Poweremotes, die anderen Charakteren ein Verhalten oder einen 
Effekt aufzwingen und keine Möglichkeit des Reagierens bieten, sind verboten.</p>

<p>11. Das Angreifen von Charakteren ohne angemessenen und nachprüfbaren Rollenspielgrund 
ist verboten. Das sofortige Töten eines Charakters direkt nach einer
 Wiederbelebung ("reskilling") ist nicht erlaubt.</p>

<p>12. Gewaltverherrlichende, brutale, perverse oder sexuelle Schilderungen 
sind im Spiel nicht erlaubt.</p>

<p>13. Schilderungen, die Wertvorstellungen und moralischen Grundsätze anderer 
Spieler verletzen, sind nicht erlaubt.</p>

<p>14. Jegliche Belästigungen, Bedrohungen oder Beleidigungen, einschließlich 
rassistischer oder sexistischer Äußerungen werden nicht toleriert. Dies gilt 
für das Spiel, das Forum und den Chat, einschließlich privater Nachrichten. 
Auf Regelverstöße anderer Spieler darf nicht ebenfalls mit einem Regelverstoß 
reagiert werden Wenn ein Verstoß vorliegt, sollte das Team von Illarion darüber 
informiert werden.</p>

<p>15. Das bewusste Ausnutzen von Spielfehlern ist verboten. Wenn ein Fehler 
im Spiel gefunden wird, sollte dieser an das Team von Illarion gemeldet werden; 
vorzugsweise durch einen Eintrag im Bugtracker.</p>

<p>16. Gamemaster sind dafür zuständig, Spielern zu helfen, Events zu veranstalten 
und für Ordnung im Spiel zu sorgen. Die Gamemaster organisieren auch die Fraktionen
 Cadomyr, Galmair und Runewick. Somit sind alle Anfragen, die die Spielwelt betreffen,
 an sie zu richten.</p>

<p>17. In den Foren ist das Erstellen von sinnlosen oder themenfremden Beiträgen 
zu unterlassen.</p>

<p>18. Die Verwendung von Zusatzprogrammen, die das Verhalten des Spiels beeinflussen,
 ist nicht gestattet.</p>
 
