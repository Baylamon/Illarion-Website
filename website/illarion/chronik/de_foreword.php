<h2>Das Vorhaben</h2>

<?php Page::cap('J'); ?>
<p>ahre gingen in das Land, die Anfänge verblassten.</p>

<p>Aufzeichnungen und Wissen gingen verloren. Um dem vorzubeugen, fanden sich Bewohner
von Troll's Bane zusammen, ihre Geschichte zu erforschen.</p>

<p>Die Bibliothek des Grauen Lichts erwies sich als eine Fundgrube. Doch blieben die
