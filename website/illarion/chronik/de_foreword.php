<h2>Die Anfänge: Eine Siedlung entsteht</h2>

<?php Page::cap('E'); ?>
<p>rste Gebäude wurden errichtet, ein kleines Dorf entstand. Eliza war eine der ersten,
die diesen Neubeginn nutze und mit Neuankömmlingen zusammen den Laden als Zentrum des
Dorfes baute. Weitere wichtige Gebäude kamen im Laufe der Jahre hinzu.</p>

<p>Der Magierturm als Wach- und Schutzturm, eine Werkstatt für die Bevölkerung und eine
hölzerne provisorische Taverne im Norden (die heute nicht mehr steht).</p>

<p>Das Graue Licht und die Graue Rose erbauten die Bibliothek, das Graue Licht später
die heutige Taverne, die Händler von Silverstar ihren kleinen Laden, Schreine für die
Götter wurden gebaut und erst in den letzten Jahren entstanden das Seahorse und
Silberbrand, die unterirdische Zwergenstadt.</p>

<p>Das Dorf wuchs und wuchs, wurde zu einer kleinen Stadt, bekam weitere Straßen, Gassen
und Wohnhäuser dazu. Die Geschichte nahm ihren Fortgang. Persönlichkeiten traten in das
Rampenlicht des öffentlichen Lebens und verschwanden wieder aus diesem. Gilden wurden
gegründet, machten sich bekannt und versanken für die große Geschichte wieder in der
Senke der Nichtbeachtung. Manche Namen waren häufig zu hören, andere wiederum blieben
bis auf wenige Augenblicke der großen öffentlichkeit verborgen. Und es gab natürlich die
Breite Masse des Volkes, von der nie jemand etwas hörte oder erfuhr.</p>

