<h1>Die Chronik von Illarion</h1>

<h2>Die Anfänge: Ende des Alten Zeitalters</h2>

<?php Page::cap('E'); ?>
<p>in mächtiger Zauberer mit dem Namen Mordak al Kharud erlangt die Herrschaft über
fast alle der menschlichen Stämme. Doch er konnte sich mit dem, was er erreicht hatte,
nicht zufrieden geben. Und so begann er sein Herrschaftsgebiet über das Territorium der
Menschen hinaus auszuweiten. Er förderte das Misstrauen und die Vorurteile, welche die
Menschen gegenüber allen anderen Rassen haben und begann Gebiete zu erobern, die den
Menschen nicht zustanden.</p>

<p>Er ist eine legendäre Gestalt - er gab Befehle, wenige haben ihn jedoch jemals zu
Gesicht bekommen. Er war ein harter Herrscher, nicht absichtlich grausam, aber extrem
hart und kaltblütig, ein Marionettenspieler sozusagen, den außer Macht kaum etwas
Interessierte.</p>

<p>Die anderen freien Völker schmiedeten eine Allianz, um sich dieser Bedrohung
entgegenzustellen. Letzten Endes wurde sein Reich zerstört und er selbst getötet.</p>

<p>Das Volk der Menschen, die durch seinen Willen und seine Macht allein geeint waren,
brachen auseinander. überall entflammten kleinere Kriege und Kämpfe. Die Menschen
versuchten, dem Zorn der anderen Völker zu entgehen, und verursachten durch ihre Flucht
Chaos und Aufruhr, wo immer sie auftauchen. Ein allgemeiner Rückgang von Werten und
Recht und Ordnung war festzustellen. Schlechte Ernten trugen zu dieser kritischen
Situation bei.</p>

