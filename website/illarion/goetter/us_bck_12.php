<h2>Brás - Month of Immolation</h2>

<p>Brás is named after Brágon, the third known creator god. He gave the sun and the fire to the world. The longest day in the year followed by the shortest night is the 24th of Brás. In this night, elves celebrate the feast of fire, in which on seas and rivers small, carven wood pieces swim and carry many candles. The humans celebrate the month differently. Salkmar and Gynk have a long feast where all kinds of artists demonstrate their skills. In Albar just the nobles usually celebrate, but their decadent feasts lasts through the whole of Brás. It's not unusual that there is a special tax enforced in many Albarian regions to finance this.</p>

