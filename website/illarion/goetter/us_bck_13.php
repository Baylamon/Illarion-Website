<p>ldan is the god of the element Spirit. With his pensive attitude, one might conclude that his mind left the world and he sought refugee in the ivory tower of his thoughts. Wisdom is his mark, yet does he prefer to remain quiet when other shout out their foolishnesses. Eldan might appear as a mage, scholar, sometimes as a beggar or drunken philosopher; but always will he carry a wooden pole-staff. He avoids the the big cities or large crowds and prefers solitude.</p>

<h2>Followers</h2>
<p>Eldan has never been too active amongst mortals. Very few people pray to him. As he was rarely
heard or seen in public, only few know how to identify him. His believers do not talk much
about him, and there are many rumors that he still wanders around in Illarion in disguise.
People who follow him are often hermits, or people who love drinks and long philosophical talks.
All claim to have met him personally, and instead of praying, they talk or discuss at forlorn places - or in Runewick.
As all elder gods, he is worshipped at the Temple of the Five, east of Galmair. 
There is also the Chapel of the Five, east of the Kantabi Desert, where the name of the Five is held in honour.</p>

<h2>Eldas - Month of Abrosia</h2>

<p>Eldas was named after the creator god Eldan. This month is in all cultures a month of peace and comtemplation. People recollect on the things the live gave them and commemorate to the people who has less luck then they. Its not unusal to abstain from a beloved or inured thing to remember its worth during the time of Eldas. Halflings of Yewdale celebrat in the last week of Eldas the "Burrow Day". It bears in remembrance the deprivations of the resistance at the time of the fall of Firnismill. Especially in the countryside many Halflings do still celebrate these days in a quite traditional way. For the whole week they withdraw into the woods and spend the nights in self-made burrows or hollows. In this week they only feed upon the fruits of the forest. No fire is allowed to be lit. At the end of the week they return in a procession to their village. Thereby a great clamor and noise is performed and the participants of the procession are armed, thus symbolizing the recapture of the areas sized by the Orcs. Nowadays in the towns and with Halflings of the upper class it has become common practice to stay this week in tents erected in the garden or the inner courtyard of their houses. There sometimes even meat dishes are served but they are decorated in the form of fruits and mushrooms.</p>

