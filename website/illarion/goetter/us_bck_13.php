<h1>Eldan - God of Spirit </h1>

<?php cap(E); ?>
<p>ldan is the god of the element Spirit. With his pensive attitude, one might conclude that his mind left the world and he sought refuge in the ivory tower of his thoughts. Wisdom is his mark, yet does he prefer to remain quiet when other shout out their foolishnesses. Eldan may appear as a wise mage, scholar, beggar or even a drunken philosopher but will always carry a wooden staff. He avoids the the big cities or large crowds and prefers solitude.</p>

<h2>Followers</h2>
<p>Eldan has never been very active among mortals. Very few people pray to him. Because he was rarely seen in public, only a few know how to identify this god. 
His worshipers are secretive and there are strange rumors of him wandering around Illarion in disguise.
People who follow him are often hermits, or people who love their wine with long philosophical discussions. 
These followers claim to have met Eldan personally, and instead of praying, they talk or discuss at forlorn places - or in Runewick.
He is also worshipped at the Temple of the Five east of Galmair and the Chapel of the Five east of the Kantabi Desert.</p>

<h2>Eldas - Month of Abrosia</h2>

<p>Eldas was named after the creator god Eldan. This month is in all cultures a month of peace and comtemplation. 
People reminisce about things that life gave them and remember the less fortunate in their prayers. 
The more devout could even abstain from their beloved or some favorite item they possess in order to remember the worth of all during this time of Eldas.</p>

<p>The Halflings of Yewdale celebrate the last week of Eldas called "Burrow Days" which signifies the resistance at the time of the fall of Firnismill. Traditionally they withdraw into the woods for the entire week and spend the nights in self-made burrows or hollows only eating fruits of the forest as no fire is allowed. At the end of the week they return in a procession to their village creating a great clamor and noise as all are armed, thus symbolizing the recapture of the areas seized by the Orcs. The more modern halflings that live in towns along with the upper class of the race modify this practice by erecting tents in their gardens or maybe an inner courtyard of their house. Sometimes even a meat dish may be served yet care is made to elaborately decorated it in the form of fruits and mushrooms.</p>

