<h2>Followers</h2>
<p>Tanora's religion dominates the lizard
society. Many of her temples are located at the coast. In the native language of the lizardman she is called
Zeshhel-pheey-arrr, almost unspeakable for the human tongue, and hence commonly spelled Zelphia. 
The lizardmen always knew her under this name and members of other peoples might even think that Zelphia is in fact another goddess. 
As all elder gods, he is worshipped at the Temple of the Five, east of Galmair. 
There is also the Chapel of the Five, east of the Kantabi Desert, where the name of the Five is held in honour.
As faith needs no house but one's mind and soul, she is also worshipped at remarkable sites at the coast of Illarion such as Cape Farewell, south of Cadomyr.</p>

<p>Most Tanora believers are Lizards, sailors, or even elves. Many healers pray to her too. Her
followers believe that she is a very powerful righteous goddess. Since most of her lizard
priests follow ancient traditions, some might be quite fanatic believers who crush evil beings
wherever they can, while many other followers are very peaceful in nature. Tanora followers
believe that the world and the magic are all one flow, and life springs from it where ever the
goddess touches it. They think, that when they die, they will have to cross an endless sea. 
When they have been good beings, the goddess may even send them a boat to bring them ashore. 
If they have been bad, they must swim and get a stone pile over the sea. For
every bad thing they did in life, a new stone is added to their pile. If the stone pile is too
heavy, they have to wait for other swimmers who show mercy and take one or another stone with
them. When they drown, the goddess will send them back to live as an animal that serves the mortals
until the guilt is cleaned.</p>

<h2>Tanos - Month of Flood</h2>

<p>The second month of spring is named after the creator-goddess Tanora, who is supposed to be identical with the water-goddess "Zelphia" of the lizard people. A harbour feast is held in many coastal towns such as Runewick. The ice has usually completely melted by this time and ships can sail out again.</p>

