	<h2>Game Concepts</h2>

<ul>
	<li class="question"><a id="gc1"></a><strong>What is an RPG and what makes Illarion special?</strong></li>
	
		<p>Role playing games (RPG) are a popular game genre. Players take on the role of a fantasy character and are able to do many things that may be impossible in Real Life. Illarion takes the concept of Role playing further than other games, players are expected to immerse themselves completely into the life of their character. Whilst playing Illarion you cease to be John/Jane Smith from New York and instead take on the role of a fictional character by feeling, saying, and thinking what he or she would. Illarion is set in a medieval world with no electricity, cars, televisions or power tools. Whilst playing you are expected to refrain from mentioning anything Out Of Character (OOC).</p>
		

	<li class="question"><a id="gc2"></a><strong>What are CMs and GMs?</strong></li>
	
		<p>A CM is a Community Manager. Their role is to assist new players and help resolve issues or conflicts between players. They should be the first people to turn to in the event of a non-technical issue. A GM is a Game Master. The GMs provide dynamic content (quests, events, etc) as well as upholding the rules of Illarion.</p>
		

	<li class="question"><a id="gc3"></a><strong>Why do some of the players talk funny?</strong></li>
		
		<p>First of all, Illarion is played by players from all around the world. So it's no wonder that they don't speak the same language. English and German are the most common languages and it is a matter of courtesy to switch language to a language commonly understood when encountering other players. Special dialects could be applied as well. Most players try to make their characters sound medieval without using Old English as many people would struggle to understand. Abbreviations and chat slang are also against the rules. Take your time using capital letters and punctuation marks too.<BR> Bad Example: "ok hi wanna buy sword lol!!!111"<BR> Good Example: "Greetings, noble sir. May I draw your attention on this well crafted blade, a blade I'd like to offer to you for a reasonable price?"</p>
		

