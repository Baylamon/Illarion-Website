	<h2>First Steps Into Illarion</h2>

<ul>
	<li class="question"><a id="fs1"></a><strong>I just started playing and don't know what to do!</strong></li>
		
		<p>There is a tutorial that will guide you through your first minutes of the game, Afterwards you're free to do whatever you like. You could become a proud Knight, a dedicated Priest, an industrious Crafter, a prudent Druid or a mysterious Mage.</p>

	<li class="question"><a id="fs2"></a><strong>How can I attack and kill somebody?</strong></li>
	
		<p>You can either right click on a character or monster and choose "Attack", or you can hold down CTRL and left click the character or monster. Please respect that it is only allowed to attack other characters when there is a valid role play reason to do so. You should also give your target the chance to interact before and during a fight. Emotes that show your character drawing his or her weapon, or moving in an intimidating fashion to another character are appreciated.</p>
		

	<li class="question"><a id="fs3"></a><strong>How do I pick up items?</strong></li>
		
		<p>You may move the item by "Dragging and Dropping" it to a free place in your inventory. This process is similar to the way icons are moved on Windows/Linux/MacOS-desktops. Your inventory is composed of your character's clothing (which is the spaces on top of the image of a man), the belt (which is the six spaces under your character's clothing/armour) and a bag. The bag must be worn and can be opened to be able to use it. If you want to open the bag of your character, perform a double click or press 'B'.</p>

		
	<li class="question"><a id="fs4"></a><strong>How do I use or equip items?</strong></li>
	
		<p>To equip an item, move the item you want to equip to a free slot in your inventory (the 'man' at the right of the screen). If you hold your mouse over a slot it shows you what you can put here. The slot has to be free to put something new in it. To do this, move the previous item somewhere else (e.g in the bag of your character). This works for weapons, armour or anything else you wear. If you want to use an item (e.g. eating an apple), right click and choose "Use". Alternatively you can hold SHIFT and click the item and release.</p>

